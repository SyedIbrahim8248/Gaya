package ibrahimjava;

public class _02_aVariables {
    public static void main(String[] args) {
        int x = 7;
        System.out.println(x);
        x = x-2;
        System.out.println(x);
        x = x+20;
        System.out.println(x);
        x = x * 10;
        System.out.println(x);
        x = x/10;
        System.out.println(x);
    }
}
