package ibrahimjava;

public class _011_Relationaloperators {
    public static void main(String[] args) {
        System.out.println(8 >= 9);
        System.out.println(8 < 4);
        System.out.println(8 > 4);
        System.out.println(8 == 8);
        System.out.println(8 <= 9);
        System.out.println(8 <= 8);
        System.out.println(6 * 7 == 42);
        System.out.println(6 * 7 != 42);
        boolean x = (5>3);
        System.out.println(x);
    }
}
