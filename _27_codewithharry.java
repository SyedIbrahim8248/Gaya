package Booksolutionjava.java;

public class _27_codewithharry {
    //wap to sum first n even numbers using while loops
    public static void main(String[] args) {
        int sum = 0;
        int n = 3;
        for (int i = 0; i < n; i++) {
            sum = sum +(2*i);
        }
        System.out.println("Sum of enev numbers: " + sum);// 0, 2,4 = 6
        System.out.println(sum);
    }
    }

