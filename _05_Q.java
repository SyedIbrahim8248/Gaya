package Booksolutionjava.java;

public class _05_Q {
    public static void main(String[] args) {
        double x ;
        x = Math.max(22,33);
        System.out.println("maxium value is " +x);
        x = Math.min(22,33);
        System.out.println("minium value is " +x);
        x = Math.sqrt(64);
        System.out.println("square value is " +x);
        x = Math.abs(-55);
        System.out.println("abs value is " +x);
        x = Math.pow(2,3);
        System.out.println("power value is " +x);
        x = Math.pow(2,-3);
        System.out.println("power value is " +x);
        x = Math.log(3);
        System.out.println("log value is " +x);
        x = Math.ceil(3);
        System.out.println("ceil value is " +x);
        x = Math.floor(3);
        System.out.println("floor value is " +x);

    }
}
