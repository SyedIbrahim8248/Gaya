package Javabygabsclasses.java;

public class _04d_dowhileloops {
   // WAP to print 6 times gabs classes, 1 times learning java and 5 times welcome to gabs
   public static void main(String[] args) {
       int i = 1;
       do {
           System.out.println("GABS CLASSES");
           i++;
       } while(i<7);
       System.out.println("Learning Java");
       i = 1;
       do {
           System.out.println("Welcome to Gabs");
           i++;
       } while(i<6);

       }
   }

