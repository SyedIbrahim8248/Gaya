package Javabygabsclasses.java;

public class _04_loops {
    //WAP to print 5 times GABS CLASSES
    public static void main(String[] args) {
        for(int i=1;i<=5;i++)
            System.out.println("GABS CLASSES");
        //WAP to print 6 times GABS CLASSES then print 1 time LEARNING JAVA then print 5 timesWELCOME TO GABS
        for(int i=1;i<=6;i++)
            System.out.println("GABS CLASSES");
        System.out.println("LEARNING JAVA");
        for(int i=1;i<=5;i++)
            System.out.println("WELCOME TO GABS CLASSES");
        //WAP to print 1,2,3,4,5
        for(int i=1;i<=5;i++)
            System.out.println(i);
        //WAP to print 2,4,6,8,10
        for (int i=2;i<=10;i=i+2)
            System.out.println(i);
        //wap to print 5,4,3,2,1
        int i;
        for(i=5;i>0; i--) {
            System.out.println(i);
        }
    }
}
