package ibrahimjava;

public class _02_VariablesBasics {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        System.out.println(x + 7);
        System.out.println(x * 2);
        int y; // decleration
        y = 20;
        System.out.println(y);
        System.out.println(x * y);
        x = x * 5;
        System.out.println(x);
        x =  x - 3;
        System.out.println(x);
    }

}
